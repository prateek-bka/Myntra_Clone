let orderDataArr = JSON.parse(localStorage.getItem("selectWomenProduct")) || [];
let container = document.getElementById("container");

let appendData = (data) => {
    container.innerHTML = null;
    data.forEach((elem) => {
        let div = document.createElement("div");
        div.id = "box";
        let img = document.createElement("img");
        img.src = elem.image;
        let title = document.createElement("h4");
        title.innerText = elem.title;
        let categories = document.createElement("p");
        categories.innerText = elem.category;
        let price = document.createElement("p");
        price.innerText = +elem.price;
        let save = document.createElement("p");
        save.innerText = elem.save;
        let discount = document.createElement("p");
        discount.innerText = +elem.discounted_price;
        let btn = document.createElement("button");
        btn.innerText = "Add to Cart";
        btn.addEventListener("click", function () {
            addDetails(elem);
        });

        div.append(img, title, categories, price, save, discount, btn);
        // console.log(div);

        container.append(div);
    });
};

appendData(women);


function addDetails(elem) {
    let data1 = {
        img: elem.image,
        title: elem.title,
        categories: elem.category,
        price: elem.price,
        save: elem.save,
        discount: elem.discounted_price,
    };
    
    orderDataArr.push(data1);
    // console.log(data1);
    localStorage.setItem("selectWomenProduct", JSON.stringify(orderDataArr));
    alert("Item added to the Cart....");
}


//price select options

let change = () => {
    // console.log(document.getElementById("sortBy").value);
    let changeValue = document.getElementById("sortBy").value;
    if (changeValue == "Lowest_Price") {
        sortLH();
    } else if (changeValue == "Highest_Price") {
        sortHL();
    }
}

let sortLH = () => {
    // console.log("Low to High");
    women = women.sort(function (a, b) {
        return a.discounted_price - b.discounted_price;
    });
    // console.log(men)
    appendData(women);
}

let sortHL = () => {
    // console.log("High to Low");
    women = women.sort(function (a, b) {
        return b.discounted_price - a.discounted_price;
    });
    // console.log(men)
    appendData(women);
}







