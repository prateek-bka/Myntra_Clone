let change = () => {
    // console.log(document.getElementById("sortBy").value);
    let changeValue = document.getElementById("sortBy").value;
    if (changeValue == "Lowest_Price") {
        sortLH();
    } else if (changeValue == "Highest_Price") {
        sortHL();
    }
}

