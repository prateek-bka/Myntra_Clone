let orderDataArr = JSON.parse(localStorage.getItem("selectProduct")) || [];
let container = document.getElementById("container");

let appendData = (data) => {
    container.innerHTML = null;
    data.forEach((elem) => {
        let div = document.createElement("div");
        div.id = "box";
        let img = document.createElement("img");
        img.src = elem.image;
        let title = document.createElement("h4");
        title.innerText = elem.title;
        let categories = document.createElement("p");
        categories.innerText = elem.category;
        let price = document.createElement("p");
        price.innerText = +elem.price;
        let save = document.createElement("p");
        save.innerText = elem.save;
        let discount = document.createElement("p");
        discount.innerText = +elem.discounted_price;
        let btn = document.createElement("button");
        btn.innerText = "Add to Cart";
        btn.addEventListener("click", function () {
            addDetails(elem);
        });
        // console.log(typeof(+elem.discounted_price));

        div.append(img, title, categories, price, save, discount, btn);
        // console.log(div);

        container.append(div);
    });
};

appendData(kids);




function addDetails(elem) {
    let data1 = {
        img: elem.image,
        title: elem.title,
        categories: elem.category,
        price: elem.price,
        save: elem.save,
        discount: elem.discounted_price,
    };

    orderDataArr.push(data1);
    // console.log(data1);
    localStorage.setItem("selectProduct", JSON.stringify(orderDataArr));
    alert("Item added to the Cart....");
}

